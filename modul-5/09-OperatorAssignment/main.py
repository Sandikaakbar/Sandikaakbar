# Operasi yang dilakukan dgn meringkas 

a = 5 
print('nilai a = ', a)

a += 1 #artinya a = a + 1
print('nilai a menjadi', a)

a -= 1 #artinya a = a - 1
print('nilai a menjadi', a)

a *= 5 #artinya a = a * 5
print('nilai a menjadi', a)

a /= 2 #artinya a = a / 2
print('nilai a menjadi', a)


# modulus dan floor division

b = 10
print('nilai b = ', b)

b %= 3
print('nilai b menjadi = ', b)

b = 10
print('nilai b = ', b)

b //= 3
print('nilai b menjadi = ', b)

#eksponen/pangkat
a = 5 
print('nilai a =', a)
a **= 3
print('nilai a menjadi =', a)

#bagaimana jika operasi adalah logika
# OR, AND

# or (|)
c = True
print('nilai c =', c)
c |= False
print('nilai c menjadi =', c)

#and (&)
c = False
print('nilai c =', c)
c &= False
print('nilai c menjadi =, c')